// Mathematical operations

#include <iostream>
#include <cmath>

using namespace std;

int main() {
    
    cout << pow(2,3) << endl;                                                                       // Power        
    cout << sqrt(25) << endl;                                                                       // Square root     
    cout << round(2.1) << endl;                                                                     // Rounding  
    cout << ceil(2.1) << endl;                                                                      // Ceil
    cout << floor(2.9) << endl;                                                                     // Floor 
    cout << fmax(3,5) << endl;                                                                      // Maximum    
    cout << fmin(3,5) << endl;                                                                      // Minimum                                                               

    return 0;
}