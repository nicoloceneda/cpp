#include <iostream>

using namespace std;

int main()
{
    char letter = 'A';                                                                              // Single character
    cout << letter << endl;

    string phrase = "Pharase";                                                                      // More than one character
    cout << phrase << endl;

    int number_int = 50;                                                                            // Integer
    cout << number_int << endl;

    double number_float = 50.1;                                                                     // Decimal
    cout << number_float << endl;

    bool is_true = true;                                                                            // Boolean
    cout << is_true << endl;

    return 0;
}