// Declare and initialize various types of variables

#include <iostream>
using namespace std;

int main() {
    
    char letter = 'A';                                                                              // Single character
    cout << letter << endl;

    string phrase = "Pharase";                                                                      // More than one character
    cout << phrase << endl;

    int numberInt = 50;                                                                             // Integer
    cout << numberInt << endl;

    double numberFloat = 50.1;                                                                      // Decimal
    cout << numberFloat << endl;

    bool isTrue = true;                                                                             // Boolean
    cout << isTrue << endl;

    const float pi = 3.14;                                                                          // Constant variable
    cout << pi << endl;

    return 0;
}